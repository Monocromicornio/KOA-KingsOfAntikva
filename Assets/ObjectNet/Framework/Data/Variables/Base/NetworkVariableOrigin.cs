namespace com.onlineobject.objectnet {
    /// <summary>
    /// Enum representing the origin of a network variable.
    /// </summary>
    public enum NetworkVariableOrigin {
        /// <summary>
        /// Indicates that the network variable is primitive.
        /// </summary>
        Primitive,

        /// <summary>
        /// Indicates that the network variable is wrapped.
        /// </summary>
        Wrapped
    }
}